def print_underline(count: int, symbol: str = "=") -> None:
    """
    The print_underline function prints a line of characters to the console.

    :param count: int: Specify the number of times to print the symbol
    :param symbol: str: Specify the character that will be used to print the underline
    :return: None
    :doc-author: Trelent"""
    print(symbol * count)
